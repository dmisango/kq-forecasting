# ENDPOINT 3 — LATEST DB FORECAST  (fast path — no inference)
# ═════════════════════════════════════════════════════════════════════════════
@app.get(
    "/forecast/latest",
    response_model=ForecastResponse,
    summary="Retrieve the latest stored forecast",
    tags=["Forecasts"],
    description=(
        "Returns the most recently stored forecast for a given route from the "
        "SQLite database — **no model inference is performed**.  This is the "
        "fast path for the Streamlit dashboard and revenue management system: "
        "the pipeline runs once daily and pre-computes forecasts; this endpoint "
        "simply serves the pre-computed results.  "
        "Response time is typically < 50 ms."
    )
)
def forecast_latest(
    route:   str = Query('NBO-MBA', description="Route code, e.g. NBO-MBA"),
    horizon: int = Query(30,        ge=1, le=90,
                         description="Number of forecast days to return"),
    models:  str = Query('ARIMA,Standalone LSTM,ARIMA-LSTM Hybrid',
                         description="Comma-separated list of models to include"),
    conn: sqlite3.Connection = Depends(get_db)
):
    # Resolve latest successful run
    run_df = _query(conn,
        "SELECT run_id FROM pipeline_runs "
        "WHERE status='SUCCESS' ORDER BY started_at DESC LIMIT 1")
    if run_df.empty:
        raise HTTPException(
            status_code=404,
            detail="No successful pipeline run found. Run pipeline.py first."
        )
    run_id    = run_df['run_id'].iloc[0]
    model_list = [m.strip() for m in models.split(',')]
    placeholders = ','.join('?' * len(model_list))

    fc_df = _query(conn,
        f"SELECT forecast_date, model, predicted_price, "
        f"lower_ci_95, upper_ci_95, horizon_day "
        f"FROM forecasts "
        f"WHERE run_id=? AND route=? AND model IN ({placeholders}) "
        f"ORDER BY model, forecast_date",
        (run_id, route) + tuple(model_list)
    )

    if fc_df.empty:
        raise HTTPException(
            status_code=404,
            detail=f"No forecasts found for route '{route}' in run {run_id}."
        )

    # Cap to requested horizon
    fc_df = fc_df[fc_df['horizon_day'] <= horizon]

    # Build response
    forecasts = []
    best_model, best_mape = None, np.inf

    for model in fc_df['model'].unique():
        sub = fc_df[fc_df['model'] == model]
        pts = [
            ForecastPoint(
                date=row['forecast_date'],
                predicted_price=round(row['predicted_price'], 2),
                lower_ci=round(row['lower_ci_95'], 2),
                upper_ci=round(row['upper_ci_95'], 2),
                horizon_day=int(row['horizon_day'])
            )
            for _, row in sub.iterrows()
        ]
        preds = sub['predicted_price'].values
        forecasts.append(ModelForecast(
            model=model, points=pts,
            mean_price=round(float(preds.mean()), 2),
            min_price =round(float(preds.min()),  2),
            max_price =round(float(preds.max()),  2),
        ))

    # Recommend best model from DB performance_metrics if available
    perf = _query(conn,
        "SELECT model, mape FROM performance_metrics "
        "WHERE run_id=? AND route=? AND segment='Overall' "
        "ORDER BY mape ASC LIMIT 1",
        (run_id, route))
    recommended = perf['model'].iloc[0] if not perf.empty else 'Standalone LSTM'

    return ForecastResponse(
        route=route,
        generated_at=datetime.utcnow().isoformat() + 'Z',
        horizon_days=horizon,
        forecasts=forecasts,
        recommended_model=recommended,
        note=f"Sourced from pipeline run {run_id}. "
             f"Re-run pipeline.py to update forecasts."
    )


