# INFERENCE HELPERS  (stateless — called on every /forecast/custom request)
# ═════════════════════════════════════════════════════════════════════════════
def _arima_forecast(prices: np.ndarray, horizon: int) -> tuple:
    """
    Refit ARIMA on provided prices and produce a horizon-step forecast.
    Returns (forecast_array, residual_std).

    The ARIMA order is taken from the stored artifact if available; otherwise
    falls back to (1,1,1) which is a reasonable default for differenced
    airline price series.
    """
    from statsmodels.tsa.statespace.sarimax import SARIMAX
    try:
        order = getattr(registry.arima, 'order', (1, 1, 1))
        seas  = getattr(registry.arima, 'seasonal_order', (0, 0, 0, 0))
        m = SARIMAX(
            prices.astype(float),
            order=order,
            seasonal_order=seas,
            enforce_stationarity=False,
            enforce_invertibility=False
        ).fit(disp=False)
        return m.forecast(steps=horizon), float(np.std(m.resid)), m.resid
    except Exception:
        # Graceful fallback: seasonal naïve
        s = 7
        fc = np.array([prices[-(s - i % s)] for i in range(horizon)])
        return fc, float(np.std(prices) * 0.3), np.zeros(len(prices))


def _sa_lstm_forecast(prices: np.ndarray, horizon: int) -> np.ndarray:
    """
    Recursive standalone LSTM forecast.
    Scales prices with the training-time scaler, runs the model step-by-step,
    and inverse-transforms the output back to USD.
    """
    seq = registry.meta.get('seq_len', SEQUENCE_LENGTH)
    sc  = registry.price_sc
    scaled = sc.transform(prices.reshape(-1, 1)).flatten()
    hist   = list(scaled[-seq:])
    preds  = []
    for _ in range(horizon):
        win = np.array(hist[-seq:]).reshape(1, seq, 1)
        p_s = registry.sa_lstm.predict(win, verbose=0)[0, 0]
        preds.append(float(sc.inverse_transform([[p_s]])[0, 0]))
        hist.append(p_s)
    return np.array(preds)


def _hybrid_forecast(arima_fc: np.ndarray,
                     arima_resid: np.ndarray,
                     horizon: int) -> np.ndarray:
    """
    ARIMA-LSTM Hybrid: ARIMA linear component + LSTM-predicted residual
    correction.  Mirrors the thesis architecture (Section 3.5.3 / Eq. 4).
    """
    seq    = registry.meta.get('hybrid_seq_len', HYBRID_SEQ_LEN)
    res_sc = registry.res_sc
    scaled = res_sc.transform(arima_resid.reshape(-1, 1)).flatten()
    hist   = list(scaled[-seq:])
    n_exog = registry.hy_lstm.input[1].shape[-1]
    exog_ph = np.zeros((1, seq, n_exog))
    corr = []
    for _ in range(horizon):
        rw  = np.array(hist[-seq:]).reshape(1, seq, 1)
        c_s = registry.hy_lstm.predict([rw, exog_ph], verbose=0)[0, 0]
        corr.append(float(res_sc.inverse_transform([[c_s]])[0, 0]))
        hist.append(c_s)
    return np.maximum(arima_fc + np.array(corr), 0.0)


def _build_ci(predictions: np.ndarray, resid_std: float,
              confidence: float = 0.95) -> tuple:
    """Empirical symmetric CI from residual standard deviation."""
    from scipy import stats
    z = float(stats.norm.ppf((1 + confidence) / 2))
    return predictions - z * resid_std, predictions + z * resid_std


def _package_forecast(model_name: str,
                       fc: np.ndarray,
                       lower: np.ndarray,
                       upper: np.ndarray,
                       start_date: datetime) -> ModelForecast:
    points = [
        ForecastPoint(
            date=(start_date + timedelta(days=i)).strftime('%Y-%m-%d'),
            predicted_price=round(float(p), 2),
            lower_ci=round(float(lo), 2),
            upper_ci=round(float(hi), 2),
            horizon_day=i + 1
        )
        for i, (p, lo, hi) in enumerate(zip(fc, lower, upper))
    ]
    return ModelForecast(
        model=model_name, points=points,
        mean_price=round(float(fc.mean()), 2),
        min_price =round(float(fc.min()),  2),
        max_price =round(float(fc.max()),  2),
    )


