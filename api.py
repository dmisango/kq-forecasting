@app.get(
    "/performance",
    response_model=PerformanceResponse,
    summary="Model performance metrics",
    tags=["Evaluation"],
    description=(
        "Returns MAE, RMSE, MAPE and R² for each model.  "
        "If the database contains performance_metrics rows (populated once "
        "forecast dates have passed and actuals are recorded), those are "
        "returned.  Otherwise the endpoint falls back to the thesis test-set "
        "results from the 2016-2020 NBO-MBA evaluation, clearly labelled as "
        "such.  This prevents the endpoint from returning empty data before "
        "the pipeline has accumulated enough actuals."
    )
)
def get_performance(
    route: str = Query('NBO-MBA', description="Route code"),
    conn:  sqlite3.Connection = Depends(get_db)
):
    run_df = _query(conn,
        "SELECT run_id FROM pipeline_runs "
        "WHERE status='SUCCESS' ORDER BY started_at DESC LIMIT 1")

    if not run_df.empty:
        run_id = run_df['run_id'].iloc[0]
        perf   = _query(conn,
            "SELECT model, segment, segment_type, mae, rmse, mape, r2, n_obs "
            "FROM performance_metrics "
            "WHERE run_id=? AND route=? ORDER BY model, segment",
            (run_id, route))

        if not perf.empty:
            return PerformanceResponse(
                route=route,
                results=perf.to_dict(orient='records'),
                source='database'
            )

    # Fallback: thesis test-set results
    return PerformanceResponse(
        route='NBO-MBA (thesis test set)',
        results=THESIS_TEST_RESULTS,
        source='thesis_test_set'
    )


