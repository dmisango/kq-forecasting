def get_db() -> sqlite3.Connection:
    """
    Dependency-injected DB connection (FastAPI Depends pattern).
    SQLite is used here for portability; in a production KQ deployment this
    would be replaced with a PostgreSQL connection pool (e.g. asyncpg).
    """
    if not DB_PATH.exists():
        raise HTTPException(
            status_code=503,
            detail=f"Database not found at {DB_PATH}. Run pipeline.py first."
        )
    conn = sqlite3.connect(str(DB_PATH), check_same_thread=False)
    conn.row_factory = sqlite3.Row
    try:
        yield conn
    finally:
        conn.close()


def _query(conn: sqlite3.Connection, sql: str,
           params: tuple = ()) -> pd.DataFrame:
    try:
        return pd.read_sql_query(sql, conn, params=params)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"DB query failed: {e}")


