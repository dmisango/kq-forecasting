@app.post(
    "/forecast/custom",
    response_model=ForecastResponse,
    summary="On-demand forecast from user-supplied prices",
    tags=["Forecasts"],
    description=(
        "Accepts a list of historical daily prices and returns forecasts from "
        "all available trained models.  Unlike /forecast/latest, this endpoint "
        "**runs live inference** using the loaded model artifacts, so response "
        "time is longer (1–10 seconds depending on hardware).  "
        "Intended for scenario analysis, what-if pricing, and one-off route "
        "assessments where the pipeline has not yet been run for that route."
    )
)
def forecast_custom(request: CustomForecastRequest):
    if not registry.available_models:
        raise HTTPException(
            status_code=503,
            detail=(
                "No model artifacts loaded. "
                f"Check {ARTIFACT_DIR} contains the required .pkl and .keras files. "
                f"Load errors: {registry._errors}"
            )
        )

    prices   = np.array(request.prices)
    horizon  = request.horizon
    conf     = request.confidence
    z        = float(__import__('scipy').stats.norm.ppf((1 + conf) / 2))
    start_dt = datetime.utcnow().date() + timedelta(days=1)
    start_dt = datetime.combine(start_dt, datetime.min.time())

    forecasts      = []
    arima_fc_arr   = None
    arima_resid    = None
    arima_std      = float(np.std(prices) * 0.3)

    # ── ARIMA ──────────────────────────────────────────────────────────────
    if 'ARIMA' in registry.available_models:
        arima_fc_arr, arima_std, arima_resid = _arima_forecast(prices, horizon)
        lo, hi = _build_ci(arima_fc_arr, arima_std, conf)
        forecasts.append(_package_forecast(
            'ARIMA', arima_fc_arr, lo, hi, start_dt))

    # ── Standalone LSTM ────────────────────────────────────────────────────
    if 'Standalone LSTM' in registry.available_models:
        sa_fc  = _sa_lstm_forecast(prices, horizon)
        sa_std = float(np.std(sa_fc) * 0.5)   # empirical CI width
        lo, hi = _build_ci(sa_fc, sa_std, conf)
        forecasts.append(_package_forecast(
            'Standalone LSTM', sa_fc, lo, hi, start_dt))

    # ── ARIMA-LSTM Hybrid ─────────────────────────────────────────────────
    if 'ARIMA-LSTM Hybrid' in registry.available_models:
        if arima_fc_arr is None:
            arima_fc_arr, arima_std, arima_resid = _arima_forecast(prices, horizon)
        resid    = arima_resid if arima_resid is not None else np.zeros(len(prices))
        hy_fc    = _hybrid_forecast(arima_fc_arr, resid, horizon)
        hy_std   = float(np.std(hy_fc) * 0.45)
        lo, hi   = _build_ci(hy_fc, hy_std, conf)
        forecasts.append(_package_forecast(
            'ARIMA-LSTM Hybrid', hy_fc, lo, hi, start_dt))

    return ForecastResponse(
        route='CUSTOM',
        generated_at=datetime.utcnow().isoformat() + 'Z',
        horizon_days=horizon,
        forecasts=forecasts,
        recommended_model='Standalone LSTM',
        note=(
            f"Live inference on {len(prices)} input prices. "
            f"{int(conf*100)}% confidence intervals computed from "
            f"residual standard deviation."
        )
    )


