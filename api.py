class CustomForecastRequest(BaseModel):
    """
    Request body for POST /forecast/custom.
    The caller supplies raw historical daily prices; the API runs all three
    models and returns forecasts for the requested horizon.
    """
    prices: List[float] = Field(
        ...,
        min_items=10,
        description="Ordered list of historical daily average prices (USD). "
                    "Minimum 10 values; 30+ recommended for reliable results.",
        example=[310, 315, 308, 322, 318, 325, 330, 328, 335, 340,
                 338, 345, 342, 350, 348, 355, 360, 358, 362, 368,
                 365, 370, 375, 372, 378, 380, 385, 382, 388, 390]
    )
    horizon: int = Field(
        30, ge=1, le=90,
        description="Number of days ahead to forecast (1–90)."
    )
    seasonal_m: int = Field(
        7, ge=2, le=14,
        description="Seasonal period in days. Default 7 (weekly)."
    )
    confidence: float = Field(
        0.95, ge=0.5, le=0.99,
        description="Confidence level for prediction intervals (default 95%)."
    )

    @validator('prices')
    def prices_must_be_positive(cls, v):
        if any(p <= 0 for p in v):
            raise ValueError('All prices must be positive.')
        return v


