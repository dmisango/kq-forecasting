# MODEL REGISTRY  (loaded once at startup — not per-request)
# ═════════════════════════════════════════════════════════════════════════════
class ModelRegistry:
    """
    Holds all trained model artifacts in memory.  Loaded once when the API
    process starts; subsequent requests pay zero disk-read cost.

    This singleton pattern is standard in production ML APIs: model loading
    (especially Keras models) is expensive (1-5 seconds), so it must not
    happen on the hot path of each request.
    """
    def __init__(self):
        self.arima      = None
        self.sa_lstm    = None
        self.hy_lstm    = None
        self.price_sc   = None
        self.res_sc     = None
        self.exog_sc    = None
        self.encoders   = None
        self.meta       = {}
        self.loaded     = False
        self._errors    = []

    def load(self, artifact_dir: Path) -> None:
        """
        Attempt to load all artifacts.  Failures are recorded but do not crash
        the server — the /health endpoint will report which models are missing,
        and endpoints that require them will return 503.
        """
        self._errors = []
        try:
            import tensorflow as tf
            self.sa_lstm  = tf.keras.models.load_model(
                artifact_dir / 'standalone_lstm_model.keras', compile=False)
            self.hy_lstm  = tf.keras.models.load_model(
                artifact_dir / 'hybrid_lstm_model.keras', compile=False)
        except Exception as e:
            self._errors.append(f'Keras models: {e}')

        for attr, fname in [
            ('arima',    'arima_model.pkl'),
            ('price_sc', 'price_scaler.pkl'),
            ('res_sc',   'residual_scaler.pkl'),
            ('exog_sc',  'exog_scaler.pkl'),
            ('encoders', 'label_encoders.pkl'),
            ('meta',     'model_meta.pkl'),
        ]:
            try:
                setattr(self, attr, joblib.load(artifact_dir / fname))
            except Exception as e:
                self._errors.append(f'{fname}: {e}')

        self.loaded = len(self._errors) == 0

    @property
    def available_models(self) -> List[str]:
        out = []
        if self.arima   is not None: out.append('ARIMA')
        if self.sa_lstm is not None: out.append('Standalone LSTM')
        if self.sa_lstm is not None and self.hy_lstm is not None:
            out.append('ARIMA-LSTM Hybrid')
        return out


